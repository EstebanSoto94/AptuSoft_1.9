                ++totalesPeriodo2.TotOpExe;
              TotalesPeriodo totalesPeriodo4 = totalesPeriodo2;
              Decimal totMntNeto = totalesPeriodo4.TotMntNeto;
              num1 = this.lista[index].Neto;
